//--------------------------------------------------------------------
void RandomSeed() 
{
   srand( (uint)TimeGetOpCount() );
}

//--------------------------------------------------------------------
uint Random()
{
   // technically will only return numbers up to RAND_MAX (which is a signed short)
   return (uint)rand();
}

//--------------------------------------------------------------------
uint Random( uint min, uint max )
{
   uint range = (max - min) + 1U;
   uint val = rand() % range;
   return val + min;
}

//--------------------------------------------------------------------
// return a random float between 0.0f and 1.0f;
float RandomFl() 
{
   return (float)rand() / (float)RAND_MAX;
}

//--------------------------------------------------------------------
float RandomFl( float min, float max ) 
{
   float v = RandomFl();
   return min + (max - min) * v;
}

//--------------------------------------------------------------------
vec3 RandomInUnitCube()
{
   return vec3( RandomFl( -1.0f, 1.0f ), 
      RandomFl( -1.0f, 1.0f ), 
      RandomFl( -1.0f, 1.0f ) );
}